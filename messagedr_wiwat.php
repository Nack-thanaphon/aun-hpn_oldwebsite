                    <h4 style="text-indent:35px;">The ASEAN University Network – Health Promotion Network (AUN-HPN) was
                        established as part of the efforts to address public health issues and sustainable development.
                        We all are aware that the challenges facing public health have changed tremendously since the
                        beginning of this century. In our closely interconnected world, health everywhere is shaped by
                        the same influential forces, such as rapid urbanization and technological development, ageing,
                        and the globalization of unhealthy lifestyles. Under the pressure of these forces, chronic
                        non-communicable diseases have overtaken infectious diseases as the leading cause of morbidity,
                        disability, and mortality. Since the Ottawa Charter produced in 1986 through to Shanghai
                        Declaration in 2016, a large body of evidence and experience has accumulated about the
                        importance of health promotion as an integrative and cost-effective strategy and essential
                        component of health systems to respond adequately to emerging concerns. Health promotion has
                        never been timelier or more needed. </h4>

                    <h4 style="text-indent:35px;">
                        AUN-HPN has been established in 2014 with the aim to serve as a platform for ASEAN higher
                        education institutions for collaboration among themselves and with other key stakeholders for
                        the purpose of health promotion in the ASEAN region. The Network also encourages sharing of
                        knowledge, skills, and resources among the network members in order to build capacity for health
                        promotion and to develop innovative solutions to common health challenges we shared. AUN-HPN
                        hopes that we could contribute by highlighting the roles of higher education institutions and
                        enhancing existing strengths in their technical knowledge and skills for health and wellbeing of
                        the population. </h4>
                    <h4 style="text-indent:35px;">
                        One of our small but important stepping stone toward expansion of health promotion concept is
                        the development of a set of standard called “Healthy University Framework” in 2017 which
                        provides practical guidance to becoming a healthy university. The framework was launched and
                        adopted in the AUN Rectors meeting in July 2017, and circulated to all AUN member universities.
                        We would like to encourage all member universities to put this framework into practice. In the
                        future, we hope that we will be able to push forward the health promotion concept to other
                        higher education institutions in Asia. </h4>
