        <h4 class="text-justify" style="text-indent:30px;">The AUN-HPN capacity building workshop on “Leadership in Health Promotion” was organized on 7-8 May 2018 at the Ambassador Hotel, Bangkok, Thailand. The workshop was organized by Mahidol University in partnership with Thai Health Promotion Foundation (ThaiHealth). This activity is based on the recommendations of the International Advisory Committee of AUN-HPN with the aim to strengthen health promotion (HP) capacity of AUN member universities. Several renowned health promotion experts and leaders in Thailand were invited to share their experience with more than 60 participants and observers from 18 ASEAN member universities and one associate member university from Japan. The workshop also had many observers from local university and international institution. Several speakers shared their knowledge and experience with the participants on how to create change agents, how to become a HP leader, and how to advocate for HP policy at different levels, and examples of leadership in health. Representatives from participating universities also presented their HP policy movement, key outcomes, and lessons learned. There was a break-out session for participants to discuss on: (1) university policies and strategies for cultivating leaders in HP, and (2) actions to support HP leaders. A representative of each group reported to all participants a summary from their group discussion. Participants had interactive discussions with speakers and among all participants throughout the workshop. The workshop concluded with actions to be taken by the AUN-HPN Secretariat and member universities to support the cultivation of HP leaders, and the adoption of Healthy University Framework (HUF) by all member universities.</h4>
